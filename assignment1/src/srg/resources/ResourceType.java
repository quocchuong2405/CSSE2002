package srg.resources;

/**
 * Enumerates the valid types of resources
 * @version 1.0
 * @ass1
 */
public enum ResourceType {
    /**
     * Consumable resource used for flying or jumping.
     * @ass1
     */
    FUEL,
    /**
     * Consumable resource used for repairing Rooms in a Ship.
     * @ass1
     */
    REPAIR_KIT;
}
